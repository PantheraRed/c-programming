#include <stdio.h>

int main()
{
    // This is how you declare a variable
    int a, b;

    // This is how you assign a value to a variable
    a = 10;
    b = 20;

    // This is how you print a variable
    printf("a = %d\n", a); // 10
    printf("b = %d\n", b); // 20

    return 0;
}
